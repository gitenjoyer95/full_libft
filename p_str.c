#include "ft_printf.h"

int	p_str(char *s)
{
	int	len;

	len = 0;
	if (s == NULL)
	{
		return (write(1, "(null)", 6));
	}
	while (s[len] != '\0')
	{
		if (p_char(s[len]) == -1)
			return (-1);
		len++;
	}
	return (len);
}
